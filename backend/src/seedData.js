export async function seedQuestions(db) {
  const rowCount = await db.get('SELECT COUNT(*) as count FROM science_literacy_questions');
  
  if (rowCount.count === 0) {
    console.log('Seeding science and literacy questions...');
    const stmt = await db.prepare(`
      INSERT INTO science_literacy_questions 
      (subject, topic, passage, question, option_a, option_b, option_c, option_d, correct_option) 
      VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)
    `);
    
    for (const q of questions) {
      await stmt.run(
        q.subject,
        q.topic,
        q.passage,
        q.question,
        q.option_a,
        q.option_b,
        q.option_c,
        q.option_d,
        q.correct_option
      );
    }
    await stmt.finalize();
    console.log('Successfully seeded database questions.');
  }

  // Seed default parent PIN '0000' if not exists
  const pinExists = await db.get("SELECT COUNT(*) as count FROM system_config WHERE key = 'parent_pin'");
  if (pinExists.count === 0) {
    console.log("Seeding default parent PIN '0000'...");
    await db.run("INSERT INTO system_config (key, value) VALUES ('parent_pin', '0000')");
  }

  // Seed math word problems if not exists
  const wordProblemsCount = await db.get("SELECT COUNT(*) as count FROM math_word_problems");
  if (wordProblemsCount.count === 0) {
    console.log("Seeding math word problems...");
    const stmt = await db.prepare("INSERT INTO math_word_problems (question, answer, explanation) VALUES (?, ?, ?)");
    for (const wp of wordProblems) {
      await stmt.run(wp.question, wp.answer, wp.explanation);
    }
    await stmt.finalize();
    console.log("Successfully seeded math word problems.");
  }
}

